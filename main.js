// === Mobile bottom-sheet helpers ===
let __sheet, __overlay, __sheetTitle, __sheetText, __sheetHtml, __activeMoreBtn = null;

function isMobile() {
    return window.matchMedia('(max-width: 640px)').matches; // tailwind sm
}

function ensureMobileSheet() {
    if (__sheet && __overlay) return;

    __overlay = document.createElement('div');
    __overlay.id = 'detailsSheetOverlay';
    __overlay.className = 'fixed inset-0 z-[100] bg-black/50 backdrop-blur-sm opacity-0 pointer-events-none transition-opacity';

    __sheet = document.createElement('div');
    __sheet.id = 'detailsSheetMobile';
    __sheet.className = 'fixed inset-x-0 bottom-0 z-[101] translate-y-full transition-transform duration-300 will-change-transform';

    __sheet.innerHTML = `
    <div class="mx-auto max-w-md sm:max-w-lg bg-zinc-900 text-white rounded-t-2xl shadow-2xl border border-white/10">
      <div class="flex items-center justify-between p-4 border-b border-white/10">
        <h3 class="font-bold text-lg" data-details-title></h3>
        <button type="button" data-details-close class="px-3 py-1.5 rounded-md bg-white/10 hover:bg-white/15 border border-white/10">✕</button>
      </div>
      <!-- ВНИМАНИЕ: добавили data-details-scroll -->
      <div class="p-4 max-h-[70vh] overflow-y-auto" data-details-scroll>
        <div data-details-text class="text-gray-300"></div>
        <div data-details-html class="hidden"></div>
      </div>
    </div>
  `;

    document.body.appendChild(__overlay);
    document.body.appendChild(__sheet);

    __sheetTitle  = __sheet.querySelector('[data-details-title]');
    __sheetText   = __sheet.querySelector('[data-details-text]');
    __sheetHtml   = __sheet.querySelector('[data-details-html]');
    __sheetScroll = __sheet.querySelector('[data-details-scroll]'); // <-- контейнер со скроллом

    // Закрытие
    __overlay.addEventListener('click', closeDetailsMobile);
    __sheet.addEventListener('click', (e) => {
        if (e.target.closest('[data-details-close]')) closeDetailsMobile();
    });
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && !__sheet.classList.contains('translate-y-full')) closeDetailsMobile();
    });
}

function openDetailsMobile({ title, html, text }) {
    ensureMobileSheet();
    __sheetTitle.textContent = title;

    if (html) {
        __sheetHtml.innerHTML = html;
        __sheetHtml.classList.remove('hidden');
        __sheetText.classList.add('hidden');
    } else {
        __sheetHtml.innerHTML = '';
        __sheetHtml.classList.add('hidden');
        __sheetText.classList.remove('hidden');
        __sheetText.textContent = text || 'Подробностей пока нет.';
    }

    // ✅ Сбрасываем скролл в начало при КАЖДОМ открытии/переключении
    if (__sheetScroll) {
        __sheetScroll.scrollTop = 0;
        // на всякий случай обнулим и внутренние (если кто-то задаст overflow им)
        __sheetHtml?.scrollTo?.(0, 0);
        __sheetText?.scrollTo?.(0, 0);
    }

    document.body.classList.add('overflow-hidden', 'touch-none');
    __overlay.classList.remove('pointer-events-none');
    requestAnimationFrame(() => {
        __overlay.classList.add('opacity-100');
        __sheet.classList.remove('translate-y-full');
    });
}

function closeDetailsMobile() {
    __overlay.classList.remove('opacity-100');
    __sheet.classList.add('translate-y-full');
    // Сброс активной кнопки
    if (__activeMoreBtn) {
        __activeMoreBtn.classList.remove('active-more');
        __activeMoreBtn.textContent = 'Подробнее';
        __activeMoreBtn = null;
    }
    setTimeout(() => {
        __overlay.classList.add('pointer-events-none');
        document.body.classList.remove('overflow-hidden', 'touch-none');
    }, 250);
}

